function toggleFilter() {
    const overlay = document.getElementById('boLocOverlay');
    overlay.style.display = overlay.style.display === 'flex' ? 'none' : 'flex';
}

function closeFilter() {
    const overlay = document.getElementById('boLocOverlay');
    overlay.style.display = 'none';
}
